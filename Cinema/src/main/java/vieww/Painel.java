    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Sala s = new Sala();
        s.setID_movie(Integer.parseInt(IdFilme.getText()));
        s.setName_movie(NomeFilme.getText());
        s.setVagas(Integer.parseInt(Vagas.getText()));
        s.setPreco(Float.parseFloat(Preco.getText()));
        
        try{
            if(servico.inserir(s))
                JOptionPane.showMessageDialog(this, "Inserido com sucesso!");
            else 
                JOptionPane.showMessageDialog(this, "Erro ao inserir!");
        } catch(RemoteException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
